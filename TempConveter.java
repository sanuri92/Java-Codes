class TempConverter{
	public static void main(String args[]){
		double F=Double.parseDouble(args[0]);
		double C=(double)5/(double)9*(F-(double)32);
		
		System.out.println("Tempreture in Fahrenheit :"+F);
		System.out.println("Tempreture in Celsius :"+C);
	
	
	
	}




} 
