class MixVariableDisplay{
	public static void main(String args[]){
	
		int a=10;
		float b=12.5f;
		String c="Java Programming";
		boolean d=true;
		
		System.out.println("int a="+a);
		System.out.println("float b ="+b);
		System.out.println("String c="+c);
		System.out.println("boolean d="+d);
		
		 
		
	}
}

/* Answer:
int a=10
float b =12.5
String c=Java Programming
boolean d=true



*/